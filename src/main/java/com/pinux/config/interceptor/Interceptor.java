@Configuration
public class Interceptor implements WebMvcConfigurer {

    @Bean
    public LoggerInerceptor loggerInerceptor() {
        return new LoggerInerceptor();
    }

    @Override
    public void addInterceptors(InterceptorRegistry registry) {
        registry.addInterceptor(loggerInerceptor()).addPathPatterns("/**");
    }
}
